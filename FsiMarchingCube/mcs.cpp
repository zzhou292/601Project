struct position {
  float pos_x;
  float pos_y;
  float pos_z;

  position(float x = 0, float y = 0, float z = 0)
      : pos_x(x), pos_y(y), pos_z(z){};

  position operator+(const position &a) const {
    return position(a.pos_x + pos_x, a.pos_y + pos_y, a.pos_z + pos_z);
  }

  bool operator==(const position &a) const {
    return a.pos_x == pos_x && a.pos_y == pos_y && a.pos_z == pos_z;
  }

  position operator-(const position &a) const {
    return position(a.pos_x - pos_x, a.pos_y - pos_y, a.pos_z - pos_z);
  }

  position operator/(float div) const {
    return position(pos_x / div, pos_y / div, pos_z / div);
  }
};

