void add_to_mesh(position ver1, position ver2, position ver3) {
  int idx_ver1 = -1;
  int idx_ver2 = -1;
  int idx_ver3 = -1;
  for (int i = 0; i < mesh_vertices.size(); i++) {
    if (mesh_vertices[i] == ver1) {
      idx_ver1 = i;
    }

    if (mesh_vertices[i] == ver2) {
      idx_ver2 = i;
    }

    if (mesh_vertices[i] == ver3) {
      idx_ver3 = i;
    }
  }

  if (idx_ver1 == -1) {
    mesh_vertices.push_back(ver1);
    idx_ver1 = mesh_vertices.size() - 1;
  }

  if (idx_ver2 == -1) {
    mesh_vertices.push_back(ver2);
    idx_ver2 = mesh_vertices.size() - 1;
  }

  if (idx_ver3 == -1) {
    mesh_vertices.push_back(ver3);
    idx_ver3 = mesh_vertices.size() - 1;
  }

  mesh_faces.push_back(int3(idx_ver1 + 1, idx_ver2 + 1, idx_ver3 + 1));
}

